int		ft_atoi(char *str)
{
	int		i;
	int		nega;
	int		nb;

	i = 0;
	nega = 0;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			nega++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + str[i] - 48;
		i++;
	}
	if (nega % 2)
		return (-nb);
	return (nb);
}
